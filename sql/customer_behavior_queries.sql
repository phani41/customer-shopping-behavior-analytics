Q7. Segment customers into New, Returning, and Loyal customers.

SELECT
CASE
    WHEN previous_purchases <= 10 THEN 'New'
    WHEN previous_purchases <= 30 THEN 'Returning'
    ELSE 'Loyal'
END AS customer_segment,
COUNT(*) AS customer_count
FROM customer
GROUP BY customer_segment;

------------------------------------------------------------

Q8. What are the top 3 most purchased products within each category?

WITH ranked_products AS (
    SELECT
        category,
        item_purchased,
        COUNT(*) AS purchase_count,
        ROW_NUMBER() OVER (
            PARTITION BY category
            ORDER BY COUNT(*) DESC
        ) AS rn
    FROM customer
    GROUP BY category, item_purchased
)

SELECT *
FROM ranked_products
WHERE rn <= 3;

------------------------------------------------------------

Q9. Are repeat buyers more likely to subscribe?

SELECT
CASE
    WHEN previous_purchases > 5 THEN 'Repeat Buyer'
    ELSE 'Non Repeat Buyer'
END AS buyer_type,
subscription_status,
COUNT(*) AS customer_count
FROM customer
GROUP BY buyer_type, subscription_status;

------------------------------------------------------------

Q10. What is the revenue contribution of each age group?

SELECT age_group,
       SUM(purchase_amount) AS revenue
FROM customer
GROUP BY age_group
ORDER BY revenue DESC;