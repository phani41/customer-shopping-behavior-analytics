Q1. What is the total revenue generated by male vs. female customers?

SELECT gender,
       SUM(purchase_amount) AS total_revenue
FROM customer
GROUP BY gender;

------------------------------------------------------------

Q2. Which gender spends more on average purchases?

SELECT gender,
       ROUND(AVG(purchase_amount),2) AS avg_purchase
FROM customer
GROUP BY gender
ORDER BY avg_purchase DESC;

------------------------------------------------------------

Q3. Which are the top 10 products with the highest average review rating?

SELECT item_purchased,
       ROUND(AVG(review_rating),2) AS avg_rating
FROM customer
GROUP BY item_purchased
ORDER BY avg_rating DESC
LIMIT 10;

------------------------------------------------------------

Q4. Compare the average purchase amounts between different shipping methods.

SELECT shipping_type,
       ROUND(AVG(purchase_amount),2) AS avg_purchase
FROM customer
GROUP BY shipping_type
ORDER BY avg_purchase DESC;

------------------------------------------------------------

Q5. Do subscribed customers spend more? And Compare customer count, average spend, and total revenue.

SELECT subscription_status,
       COUNT(*) AS customers,
       ROUND(AVG(purchase_amount),2) AS avg_spend,
       SUM(purchase_amount) AS total_revenue
FROM customer
GROUP BY subscription_status;

------------------------------------------------------------

Q6. What percentage of purchases had discounts applied?

SELECT discount_applied,
       COUNT(*) AS purchase_count,
       ROUND(
           COUNT(*) * 100.0 /
           (SELECT COUNT(*) FROM customer),
           2
       ) AS percentage
FROM customer
GROUP BY discount_applied;

